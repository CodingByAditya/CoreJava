class LogicalOperator 
{
	public static void main(String[] args) 
	{

		System.out.println((23>12)&&(55>33));
		System.out.println((54>5)&&(54<5));
		System.out.println((60<50)&&(60>50));

		System.out.println((5<3)||(5>3));
		System.out.println((5<3)||(5>3));
		System.out.println((5<3)||(5>3));
		System.out.println((5<3)||(5<3));

		System.out.println(!(5==3));
		System.out.println(!(5>3));
	}
}
