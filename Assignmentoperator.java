class  Assignmentoperator
{
	public static void main(String[] args) 
	{
		int a = 1000;
		int b;

		//=
		b = a;
		System.out.println(b);
		b += a;
		System.out.println(b);
        b -= a;
		System.out.println(b);
        b *= a;
		System.out.println(b);
        b /= a;
		System.out.println(b);
		b %= a;
		System.out.println(b);
	}
}
